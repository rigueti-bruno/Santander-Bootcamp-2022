let num = 100;
console.log(num);

// Operações Aritméticas:

let soma = num + 5;
console.log(soma);

let subtracao = num - 40;
console.log(subtracao);

let multiplicacao = num * 6;
console.log(multiplicacao);

let divisao = num / 4;
console.log(divisao);

let potencia = num ** 10;
console.log(potencia);

let resto = num % 7;
console.log(resto);

// Biblioteca Math -> Objeto com métodos para execução de operações matemáticas:

let numb = 1.666;

console.log(Math.floor(numb)); // Arredonda o valor para baixo

console.log(Math.ceil(numb)); // Arredonda o valor para cima

let percent = 10;

console.log(percent + "%"); // Para apresentar porcentagens, precisamos concatená-los com a string "%"

console.log(percent.toString()); // converte o valor para o tipo String