// Empty - Variável Vazia:
let vall1 = 0
console.log(vall1);
console.log(typeof vall1);

let vall2 = ""
console.log(vall2);
console.log(typeof vall2);

let vall3 = []
console.log(vall3);
console.log(typeof vall3);

let vall4 = {}
console.log(vall4);
console.log(typeof vall4);

// Null - Variável Nula:

let nula = null;
console.log(nula);
console.log(typeof nula);


// Undefined - Variável Indefinida:

//console.log(indefinida);
console.log(typeof indefinida);

// Valores Nulos não podem ser definidos como verdadeiro ou falso:
let verdadeiro = nula === true;
console.log(verdadeiro);

let falso = nula === false;
console.log(falso);

// Mas, seus inversos são verdadeiros:

console.log(!nula);